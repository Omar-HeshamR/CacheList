    def __init__(self, size):
        self.head = None
        self.maxSize = size
        self.remainingSpace = size
        self.numItems = 0

    def __str__(self):
        listString = ""
        current = self.head
        while current is not None:
            listString += "[" + str(current.value) + "]\n"
            current = current.next
        return 'REMAINING SPACE:{}\nITEMS:{}\nLIST:\n{}'.format(self.remainingSpace, self.numItems, listString)  

    __repr__=__str__

    def __len__(self):
        return self.numItems
    
    def put(self, content, evictionPolicy):
        if content.size > self.maxSize: #if content size is greater than max size (do not evict anything)
            return 'Insertion not allowed'
        if content.size > self.remainingSpace: #evict items according to the eviction policy (if there is not enough space for content)
            if evictionPolicy == 'mru':
                while content.size > self.remainingSpace:
                    self.mruEvict()
            if evictionPolicy == 'lru':
                while content.size > self.remainingSpace:
                    self.lruEvict()
        if self.__contains__(content.cid) == True:
            return f'Content {content.cid} already in cache, insertion not allowed'
        else:
            if self.numItems == 0:
                self.remainingSpace = self.maxSize
                new_node = Node(content)
                self.head = new_node
                self.numItems = 1
                self.remainingSpace = self.remainingSpace - content.size
                return f"INSERTED: {content}"
            else:
                new_node = Node(content)
                new_node.next = self.head
                self.head = new_node
                self.remainingSpace = self.remainingSpace - new_node.value.size
                self.numItems = self.numItems + 1
                return f"INSERTED: {content}"

    def __contains__(self, cid):
        if self.head == None:
            return False
        previous = None
        current = self.head
        if cid == current.value.cid:
            return True
        while cid != current.value.cid:
            previous = current
            if current.next == None:
                return False
            current = current.next
        previous.next = current.next
        current.next = self.head
        self.head = current
        return True

    def update(self, cid, content): #Updates a ContentItem with a given id in the list.
        if content.size > self.maxSize:
            if self.__contains__(cid) == True:
                return 'Cache miss!'
        if self.__contains__(cid) == False:
            return 'Cache miss!'
        if self.__contains__(cid) == True:
            if content.size > self.remainingSpace + self.head.value.size:
                return 'Cache miss!'
            self.remainingSpace = self.remainingSpace + self.head.value.size - content.size
            new_node = Node(content)
            temp = self.head
            new_node.next  = temp.next
            self.head = new_node

            return f'UPDATED: {content}'


    def mruEvict(self):
        self.remainingSpace = self.remainingSpace + self.head.value.size
        self.numItems = self.numItems - 1
        temp = self.head
        self.head = self.head.next
        temp.next = None

    def lruEvict(self):
        if self.numItems == 1:
            self.clear()
        else:
            current = self.head
            prev = None
            while current.next != None:
                prev = current
                current = current.next
            self.remainingSpace = self.remainingSpace + current.value.size
            self.numItems = self.numItems - 1
            prev.next = None
        
    def clear(self):
        self.head = None
        self.numItems = 0
        self.remainingSpace = self.maxSize
        return 'Cleared cache!'


